{
    /// <summary>
    /// Describes the component to use when formatting the Vector4 value.
    /// </summary>
    public enum Axis4D
    {
        /// <summary>
        /// Use the x component of the Vector4 value.
        /// </summary>
        X,

        /// <summary>
        /// Use the y component of the Vector4 value.
        /// </summary>
        Y,

        /// <summary>
        /// Use the z component of the Vector4 value.
        /// </summary>
        Z,

        /// <summary>
        /// Use the w component of the Vector4 value.
        /// </summary>
        W
    }
}
